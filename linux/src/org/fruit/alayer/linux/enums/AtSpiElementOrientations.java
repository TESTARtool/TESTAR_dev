package org.fruit.alayer.linux.enums;


/**
 * Defines orientations an AtSpiElement can be in.
 */
public enum AtSpiElementOrientations {


    /**
     * The orientation is undefined or unknown.
     */
    Undefined,


    /**
     * The orientation of the element is horizontal.
     */
    Horizontal,


    /**
     * The orientation of the element is vertical.
     */
    Vertical,


    /**
     * The orientation of the element is both horizontal and vertical (if that's even possible).
     */
    HorizontalAndVertical


}